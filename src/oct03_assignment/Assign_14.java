package oct03_assignment;
/*
 * Q..Write a program to draw following star pattern
 
 * * * *
 * * * *
 * * * *
 */
public class Assign_14 {

	public static void main(String[] args) 
	{
		for(int i=0; i<3; i++)
		{
			for(int j=0; j<=3; j++)
			{
				System.out.print("* " );
			}
			System.out.println();
		}

	}

}
